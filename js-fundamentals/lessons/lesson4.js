// Relational or Comparison operators

// > - more than
// < - less than
// >= - more than equal
// <= - less than equal

var result = 10 > 5
console.log(result)

result = 4 > 5
console.log(result)

result = 5 < 5
console.log(result)

result = 5 <= 5
console.log(result)

result = 6 <= 5
console.log(result)

// Equality operators

var x = 1

console.log(x == '1') // lose comparison, e.g. not comparing data types, but only values
console.log(x === '1') // strict comparison, comparing both value and data type
console.log(x === 1) // strict comparison, comparing both value and data type